        <h1>PREPARACION</h1>

        <P>1. Mescla el tequila, el jugo de limón y de narranja, ajo, cebolla morada, sal y pimienta.
            Revuelva hasta que esté todo bien unido y agregue a la carne en falda. </P>
        
        <p>2. Tapa y lleva al refrigerador para que quede marinando durante 4 a 6 horas. </p>    
        <p>3. Prenda la parrilla . Retire la carne marinada y cocine en la parrilla durante 7 a 10 minutos de cada lado. 
            Retire de la parrila y deje reposar durante 5 minutos.
        </p>
        <p>4. Para las rebanadas, has cortez perpendiculares en la carne.</p>
        <p>5. Sirve tibio con tortillas y salsa. </p>
